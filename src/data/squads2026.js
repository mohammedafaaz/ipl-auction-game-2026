function normalize(str) {
  return str.toLowerCase().replace(/[^a-z0-9]/g, '');
}

// Build teamId -> array of player slugs matched against pool
// Also patches isOverseas on pool players based on 2026 data
export function buildSquadMap(playerPool) {
  const nameToPlayer = {};
  playerPool.forEach(p => { nameToPlayer[normalize(p.name)] = p; });

  const squadMap = {};

  for (const [teamId, players] of Object.entries(SQUADS_2026)) {
    squadMap[teamId] = [];

    for (const { name, isOverseas } of players) {
      const norm = normalize(name);
      let player = nameToPlayer[norm];

      // partial match for slight name differences
      if (!player) {
        const key = Object.keys(nameToPlayer).find(k => k.includes(norm) || norm.includes(k));
        if (key) player = nameToPlayer[key];
      }

      if (player) {
        // Patch isOverseas from 2026 PDF data
        player.isOverseas = isOverseas;
        squadMap[teamId].push(player.id);
      }
    }
  }

  return squadMap;
}

